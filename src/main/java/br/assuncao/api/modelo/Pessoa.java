package br.assuncao.api.modelo;



public class Pessoa {
    
    // atributos
    private String nome;
    private int idade;

    
    // GET and SET
    public String getNome() {
        return nome;
    }
    
    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }
    public void setIdade(int idade) {
        this.idade = idade;
    }

}
